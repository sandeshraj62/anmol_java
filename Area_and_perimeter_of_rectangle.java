package basic;

public class Area_and_perimeter_of_rectangle {
	public static void main(String args[])
	{
		//int ratio;
		System.out.println("ratio");
		double length=21.5;
		double breadth=10.5;
		double area_of_rectangle=(length*breadth);
		double perimeter_of_rectangle=(2*(length+breadth));
		System.out.println(" Area of rectangle is="+area_of_rectangle);
		System.out.println("Perimeter of rectangle is="+perimeter_of_rectangle);
	
		
	}

}
