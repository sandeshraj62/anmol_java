package basic;

public class Surface_Area_of_Cuboid {
	public static void main(String args[]) {
		int length=2;
		int breadth=3;
		int height=5;
		int surf_area_of_cuboid=2*((length*breadth)+(breadth*height)+(height*length));
		System.out.println("Surface area of cuboid ="+surf_area_of_cuboid);
	
				
	
	
	}

}
