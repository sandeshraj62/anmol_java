package basic;

public class Surface_Area_Of_Cylinder {
	public static void main(String args[])
	{
		double pie=3.14;
		int radius=2;
		int height=5;
		float surf_area_of_cylinder=(float)(2*pie*(radius+height));
		System.out.println("Surface Area of Cylinder ="+surf_area_of_cylinder);
	}

}
